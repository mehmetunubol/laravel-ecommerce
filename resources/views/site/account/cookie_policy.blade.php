<h1> ÇEREZ POLİTİKASI </h1>



<p>
Bu Çerez (Cookie) Uygulamaları Politikası; Egepleks Plastik Sanayi Tic. kısaca EGEPLEKS  tarafından veya onlar adına yürütülen tüm web siteleri, üçüncü parti platformlardaki markalı web siteleri (Facebook ve YouTube gibi), ve bu web siteleri veya üçüncü parti programlar üzerinden erişilen veya kullanılan uygulamalar için geçerlidir.
</p>
<p>
EGEPLEKS sitelerini kullanarak, çerezlerin bu Çerez Uygulamaları Politikası ile uyumlu şekilde kullanılmasına onay vermiş olursunuz. Eğer çerezlerin bu şekilde kullanılmasını istemiyorsanız, tarayıcınızın ayarlarını düzenlemeli veya EGEPLEKS sitelerini kullanmamalısınız. Kullandığımız çerezleri devre dışı bırakmak, EGEPLEKS sitelerindeki kullanıcı deneyiminizi etkileyebilir.
 </p>
<p>
Çerez uygulamaları nelerdir?
Çerezler, bir EGEPLEKS sitesini ziyaret ettiğinizde bilgisayarınızda (veya akıllı telefonlar ve tabletler gibi internet özellikli diğer cihazlarda) depolanabilecek dosyalar veya bilgi parçacıklarıdır. Bir çerez genellikle; alındığı web sitesinin adını, çerezin “ömrünü” (ör. cihazınızda ne kadar süreyle tutulacağı), ve genellikle tesadüfi şekilde oluşturulan kendine özgü bir sayı değeri içerir.
 </p>
<p>
Çerezleri ne amaçla kullanırız?
Çerezleri; EGEPLEKS sitelerinin kullanımını kolaylaştırmak, EGEPLEKS sitelerini ve ürünlerimizi ilgi ve ihtiyaçlarınız doğrultusunda daha iyi bir şekilde özelleştirmek amacıyla kullanırız. Çerezler aynı zamanda gelecekte sitelerimiz üzerinde gerçekleştireceğiniz faaliyet ve deneyimleri hızlandırmak amacıyla da kullanılabilir. Ayrıca çerezleri, insanların sitelerimizi nasıl kullandıklarını anlamamızı sağlayan isimsiz ve toplu istatistiki verileri bir araya getirmemize ve sitelerimizin yapılarını ve içeriklerini geliştirmemize yardımcı olmaları amacıyla kullanmaktayız. Bu veriler, sizin kimliğinizi tanımlamamızı sağlayabilecek bilgiler değildir.
 </p>
<p>
Ne tür çerezler kullanıyoruz?
EGEPLEKS sitelerinde iki tür çerez kullanılabilir; “oturum çerezleri” ve “kalıcı çerezler”. Oturum çerezleri, EGEPLEKS sitesinden ayrılana dek cihazınızda tutulan geçici çerezlerdir. Bir kalıcı çerez, cihazınızda siz onu silinceye kadar daha uzun süreli saklanmaktadır. (Çerezin cihazınızda ne kadar süreyle tutulacağı, o çereze ait süreye veya “ömre”, ve tarayıcınızın ayarlarına bağlı olarak değişecektir)
 </p>
<p>
Ziyaret ettiğiniz bazı sayfalar, piksel etiketleri (temiz gif olarak da adlandırılır) kullanarak, tanıtım faaliyetlerimizi ve web sitesi geliştirmelerimizi doğrudan desteklemekte olan üçüncü partilerle paylaşılabilecek bilgileri alabilir. Örneğin, EGEPLEKS sitelerine gelen ziyaretçilere dair web sitesi kullanım bilgileri, web sitelerimizdeki banner reklamlarının daha etkili bir şekilde görünürlüğünü sağlamak amacıyla üçüncü parti reklam ajanslarımızla paylaşılabilir. Bu bilgiler kişisel bilgilerinizle ilişkili olabilir ancak sizin kimliğinizi tanımlayamazlar.
</p>
