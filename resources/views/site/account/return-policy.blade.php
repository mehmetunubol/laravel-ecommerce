<h1> KARGO BİLGİLERİ VE İADE KOŞULLARI </h1>

<p>Ürün iade istekleri 6502 sayılı Tüketicinin Korunması Hakkındaki Kanun hükümleri esas alınarak esas alınarak aşağıdaki kriterler dâhilinde yapılmaktadır.</p>

<p>İade edilecek ürün, tarafınıza mail olarak gönderilen fatura ile fatura alt kısmında mevcut olan iade formuna iade sebebi doldurularak gönderilmelidir. Fatura olmaması durumunda tarafınıza gönderilen sevk irsaliyesi ile de gönderim yapabilirsiniz. İrsaliye üzerine ürün ile ilgili talebinizi açık bir şekilde yazmanız yeterli olacaktır.</p>

<p>
Ürün tarafınıza teslim edildikten sonra 14 iş gün içerisinde iade için tarafımıza gönderebilirsiniz.</p>


<p>
Minimun Sipariş
Minimum siparişiniz 75 TL olmalıdır.
</p> 
<p>
Ücretsiz Kargo
KDV Dahil 750 TL ve üstü alışverişlerinizde Türkiye sınırları dahilinde kilo ve boyut sınırlaması olmaksızın kesinlikle kargo ücreti ödemezsiniz.
Alışverişiniz anlaşmalı kargo şireketimizle size ulaştırılacaktır.
 
</p> 
<p>
Kargo Ücreti
Normal Kargo: 16,50 TL
Havaleli Kargo: 117,00 TL (bu kargo şekli ile ilgili merkezimizle temasa geçiniz)
 </p> 
<p>

Teslimat Süresi
Gün içinde saat 14:00’e kadar verdiğiniz siparişleriniz, 3 - 5 gün içerisinde hazırlanarak anlaşmalı kargoya teslim edilmektedir (Cumartesi-Pazar ve Resmi tatil günleri gönderim yapılamaz). 
Özel ürünlerin teslim süreleri için ayrıca merkesimizle temasa geçiniz.
 
</p> 
<p>
Ürün Teslim Alırken Dikkat Edilmesi Gereken Husular
Faturası paketin üstündeki cepte bulunmayan kargoyu teslim almayınız.
Yırtılmış, ezilmiş veya açılmış paketi kesinlikle teslim almayınız.
Faturanızdan farklı veya eksik paket teslim almayınız.
Söz konusu sorunlardan herhangi biri oluşmuşsa kargo görevlisinden tutanak tutmasını isteyininiz.
Bu uyarıları dikkate almadan teslim alırsanın ürünü sorunsuz teslim aldığınızı kabul etmiş sayılırsınız.
 
</p> 
<p>
Ödeme Şekilleri
Havale
Kredi Kartı
Kapıda ödeme (ayrıca 17,50 TL hizmet bedeli ödemeniz gerekecektir)
</p> 




